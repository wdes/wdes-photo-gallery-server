CREATE DEFINER=`wdesphotogallery`@`%` PROCEDURE `getContainerRoot`(IN `idContainer` INT(11) UNSIGNED, OUT `path` VARCHAR(1000) CHARSET utf8mb4)
    READS SQL DATA
    COMMENT 'Return the container root'
BEGIN
	SET max_sp_recursion_depth = 255;
    SELECT g.idContainerParent FROM gfs__Structure g
    WHERE g.idContainer=idContainer INTO @idContainerParent;
    IF @idContainerParent IS NULL
    THEN
        SET path = CONCAT('/',getContainerName(idContainer));
    ELSE
    	SET @temppath = "";
        CALL getContainerRoot(@idContainerParent, @temppath);
        SET path = CONCAT(@temppath,'/', getContainerName(idContainer));
    END IF;
END ;;
